    private void VerifyButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VerifyButtonMouseClicked
        Controller controller = new Controller(this.f1, this.f2);
        controller.getIntersectionBib();

        String context = "";
        int i = 0;
        if (controller.intersecao.isEmpty()) {
            showMessage("Não há interseção entre as bases", controller.intersecao.size());
        } else {
            for (BibtexEntry entry : controller.intersecao) {
                context = context.concat(String.valueOf(i)).concat(". ").concat(entry.getField("title").concat("\n"));
                i++;
            }
            showMessage(context, controller.intersecao.size());
        }

    }//GEN-LAST:event_VerifyButtonMouseClicked

