    private File openFileChooser(JTextField textfield) {
        int returnVal = jFileChooser1.showOpenDialog(this);
        File file = null;
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            file = jFileChooser1.getSelectedFile();
            if (!file.getName().endsWith(".bib")) {
                JOptionPane.showMessageDialog(rootPane, "A extensão do arquivo não é .bib", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                try {
                    // What to do with the file, e.g. display it in a TextArea
                    textfield.read(new FileReader(file.getAbsolutePath()), null);
                    textfield.setText(file.getName());
                } catch (IOException ex) {
                    System.out.println("problem accessing file" + file.getAbsolutePath());
                }
            }
        } else {
            System.out.println("File access cancelled by user.");
        }
        return file;
    }

    public void showMessage(String mensagem, int size) {
        this.IntersecoesTextArea.append(mensagem);
        this.TotalTextfield.setText(String.valueOf(size));
    }

